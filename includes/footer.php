        newsletterForm.addEventListener('submit', async function(e) {
          e.preventDefault();

          const formData = new FormData(this);
          const email = formData.get('email').trim();
          const button = this.querySelector('button');
          const arrowIcon = document.getElementById('newsletterBtnIcon');
          const loadingIcon = document.getElementById('newsletterLoading');
          const btnText = document.getElementById('newsletterBtnText');

          // Validate email
          if (!email) {
            showNewsletterMessage('Please enter your email address.', 'error');
            return;
          }

          if (!isValidEmail(email)) {
            showNewsletterMessage('Please enter a valid email address.', 'error');
            return;
          }

          // Show loading state
          if (arrowIcon) arrowIcon.classList.add('hidden');
          if (loadingIcon) loadingIcon.classList.remove('hidden');
          if (btnText) btnText.textContent = 'Subscribing...';
          if (button) button.disabled = true;

          try {
            const response = await fetch('ajax/newsletter.php', {
              method: 'POST',
              headers: {
                'Content-Type': 'application/x-www-form-urlencoded',
              },
              body: `email=${encodeURIComponent(email)}`
            });

            // Check if response is ok
            if (!response.ok) {
              throw new Error(`HTTP error! status: ${response.status}`);
            }

            // Get response text first
            const responseText = await response.text();

            // Try to parse as JSON
            let data;
            try {
              data = JSON.parse(responseText);
            } catch (parseError) {
              console.error('JSON parse error:', parseError, 'Response:', responseText.substring(0, 100));
              throw new Error('Invalid server response');
            }

            if (data.success) {
              showNewsletterMessage(data.message, 'success');
              newsletterForm.reset();
            } else {
              showNewsletterMessage(data.message, 'error');
            }
          } catch (error) {
            console.error('Newsletter subscription error:', error);
            showNewsletterMessage('An error occurred. Please try again.', 'error');
          } finally {
            // Hide loading state
            if (arrowIcon) arrowIcon.classList.remove('hidden');
            if (loadingIcon) loadingIcon.classList.add('hidden');
            if (btnText) btnText.textContent = 'Subscribe';
            if (button) button.disabled = false;
          }
        });

